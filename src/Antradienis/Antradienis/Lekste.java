package Antradienis;
public class Lekste {
	private float spindulys = 0f;
	private String spalva = "";
	
	
	public Lekste() {
	}

	public Lekste(float spindulys, String spalva) {
		super();
		this.spindulys = spindulys;
		this.spalva = spalva;
	}
	
	public float getSpindulys() {
		return spindulys;
	}
	public void setSpindulys(float spindulys) {
		this.spindulys = spindulys;
	}
	public String getSpalva() {
		return spalva;
	}
	public void setSpalva(String spalva) {
		this.spalva = spalva;
	}
	
	
	

}
